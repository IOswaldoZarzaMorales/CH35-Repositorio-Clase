// Sentencias Switch 

let nota = 7 ;

switch (nota) {
    case 5:
        console.log("En hora buena, has obtenido un sobresaliente");
        break
    case 4:
        console.log ("Buen trabajo, pero podrias hacerlo mejor");
        break
    case 3:
        console.log ("Te has obtenido un suficiente");
        break
    case 2:
        console.log ("No has aprobado por poco")
        break
    case 1:
        console.log ("No has estudiado nada trabaja un poco mas")
        break
    default:
        console.log ("Error");
        break;
}



